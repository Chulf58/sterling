// architecture-projection [S] (spec §15 via §12, adjudicated 2026-06-12): the
// Architecture & extension document is a GENERATED READ-ONLY projection over
// the extensible-set registries and their owning feature articles — never
// hand-maintained. For each set: the registry (single source of truth), the
// checks that guard it, and "extending this" quoted from the owning article's
// intended_behavior. A set with no owning article renders a LOUD gap, never
// silently drops (P5). Deterministic: no wall-clock stamps — the version line
// derives from the articles themselves, so an unchanged store regenerates
// byte-identical output.
import { writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { openProject } from './lib/project.mjs';

// The §15 extensible sets — CLOSED here on purpose, mirrored by the totality
// pin in scripts/tests/projection.test.mjs: adding a set obligates extending
// this table, and the test is the check that catches a miss.
const SETS = [
  {
    key: 'signals',
    title: 'Signals (the closed exit enum)',
    article: 'brain-signal-protocol',
    registry: 'packages/schemas/src/transient.ts — SIGNALS + SIGNAL_PAYLOADS (consumed by the brain REACTIONS table)',
    checks: 'scripts/check-totality.mjs (npm run check) + the brain totality test (packages/mcp-server brain.test)',
  },
  {
    key: 'record-types',
    title: 'Record types (durable knowledge shapes)',
    article: 'record-schemas-registry',
    registry: 'packages/schemas/src/records.ts — RECORD_TYPES (schema + fts + fileKeys per member) + SYSTEM_REASONS/DRAIN_VERBS',
    checks: 'schemas registry test (1:1, unregistered rejected) + store write validation on every create',
  },
  {
    key: 'agents',
    title: 'Agents (the roster)',
    article: 'agent-roster-and-skills',
    registry: 'agent-templates/registry.json (1:1 with templates)',
    checks: 'scripts/check-agent-registry.mjs, check-agent-prompts.mjs (§7.3 sections), check-spawn-contracts.mjs (§7.4), check-agents-visible.mjs (runtime)',
  },
  {
    key: 'hooks',
    title: 'Hooks (conduct enforcement at platform seams)',
    article: 'hooks-suite',
    registry: 'hooks/hooks.json (project-level registrations) + agent-template frontmatter (per-agent hooks)',
    checks: 'hooks.json emission check (forward-slash commands) + scripts/tests/hooks-full.test.mjs / enforcement.test.mjs',
  },
  {
    key: 'tools',
    title: 'Tools (the §10 MCP surface)',
    article: 'mcp-tool-surface',
    registry: 'packages/mcp-server/src/server.ts — registerTool set (the one write path to the store; H15 guards shell bypass)',
    checks: 'server.test.ts exact tool-list assertion + zod input schemas per tool',
  },
  {
    key: 'toolchain-adapters',
    title: 'Toolchain adapters (per-stack capability layer)',
    article: 'toolchain-adapters',
    registry: 'scripts/adapters/registry.json — name → module, fixed interface (capabilities/testPathGlobs/runCommands/runTests)',
    checks: 'checkAdapterRegistry() via scripts/check-agent-registry.mjs + adapter tests in scripts/tests/enforcement.test.mjs',
  },
];

const { cwd, store } = openProject();
const articles = store.query({ types: ['feature_article'], cap: 1000 });
const bySlug = new Map(articles.map((a) => [a.slug, a]));

const sections = SETS.map(({ key, title, article, registry, checks }) => {
  const owner = bySlug.get(article);
  const extending = owner
    ? `**Extending this:** ${owner.intended_behavior}\n\nFull article: \`${article}\` (v${owner.version}, ${owner.updated_at}) — Articles tab or \`knowledge_query\`.`
    : `**ARTICLE GAP** — no owning feature article \`${article}\` in the store. The projection cannot describe extension obligations for this set; run the coverage sweep and create the article.`;
  return `## Extensible set: ${key} — ${title}\n\n- Registry: ${registry}\n- Checks: ${checks}\n\n${extending}\n`;
});

const newest = articles.map((a) => a.updated_at).sort().at(-1) ?? 'empty store';
const md = `<!-- GENERATED by scripts/architecture-projection.mjs — DO NOT EDIT.
     Read-only projection from the owning articles + registries (spec §15 via §12).
     Regenerate: node scripts/architecture-projection.mjs (store state as of ${newest}). -->

# Sterling architecture & extension document (generated)

Sterling documents and extends itself by the same registry + checked-contract +
fail-loud discipline it imposes (§15). For every extensible set below: the
registry is the single source of truth, the named checks guard it, and the
"extending this" guidance is quoted from the set's owning feature article —
the durable store is authoritative; this file is a projection of it.

${sections.join('\n')}`;

writeFileSync(join(cwd, 'architecture.md'), md);
store.close();
console.log(`architecture.md written (${SETS.length} sets, ${SETS.filter((s) => !bySlug.get(s.article)).length} article gap(s))`);
