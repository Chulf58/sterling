// Bundle-freshness check (invariant 4; R2 board 7cde1448) — the audited hook
// SOURCES only matter if the shipped bundles are what actually runs. Rebuilds
// every scripts/hooks/h*.mjs with esbuild options IDENTICAL to build-hooks.mjs
// into a temp dir and byte-compares against the tracked hooks/*.mjs bundles;
// any divergence fails loud naming the stale bundle. Requires built packages
// (bundles resolve workspace dist files — same caveat as check-totality).
//   node scripts/check-bundles-fresh.mjs
import { build } from 'esbuild';
import { readdirSync, readFileSync, mkdtempSync, rmSync, existsSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..');
const srcDir = join(here, 'hooks');
const bundleDir = join(root, 'hooks');

const entries = readdirSync(srcDir).filter((f) => f.startsWith('h') && f.endsWith('.mjs'));
const tmp = mkdtempSync(join(tmpdir(), 'sterling-bundle-check-'));
const stale = [];
try {
  for (const entry of entries) {
    // options mirror scripts/build-hooks.mjs exactly — divergence here would
    // false-flag every bundle, so keep the two in sync
    await build({
      entryPoints: [join(srcDir, entry)],
      outfile: join(tmp, entry),
      bundle: true,
      platform: 'node',
      format: 'esm',
      target: 'node24',
      banner: { js: '// GENERATED by scripts/build-hooks.mjs — do not edit; edit scripts/hooks/ and rebuild.' },
    });
    const shipped = join(bundleDir, entry);
    if (!existsSync(shipped)) {
      stale.push(`${entry} (no shipped bundle at hooks/${entry})`);
      continue;
    }
    if (!readFileSync(join(tmp, entry)).equals(readFileSync(shipped))) stale.push(entry);
  }
} finally {
  rmSync(tmp, { recursive: true, force: true });
}

if (stale.length) {
  console.error('bundle freshness FAILED — shipped hooks/ bundles do not match a fresh build of their sources:');
  for (const s of stale) console.error(`  ${s}`);
  console.error('Rebuild and commit: node scripts/build-hooks.mjs');
  process.exit(1);
}
console.log(`bundle freshness: ok (${entries.length} bundle(s) byte-identical to a fresh build)`);
